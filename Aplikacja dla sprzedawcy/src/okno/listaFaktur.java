public void wypiszDB() throws SQLException {
        String query;
        model.setRowCount(0);
        SimpleDateFormat DATE_FORMAT = new SimpleDateFormat("yyyy-MM-dd");

        String dateDO = "2100-12-31";
        String dateOD = DATE_FORMAT.format(jXDatePicker2.getDate());

        try {
            dateDO = DATE_FORMAT.format(jXDatePicker3.getDate());
        } catch (NullPointerException ex) {
            dateDO = "2100-12-31";
        }

        query = "   SELECT * FROM Faktura  "
                + " WHERE   DataWystawienia BETWEEN '" + dateOD + "' AND '" + dateDO + "' AND numer "
                + "LIKE '%" + jXSearchField1.getText() + "%';";

    
        ResultSet rs = con.querySelect(query);

        while (rs.next()) {
            DecimalFormat df = new DecimalFormat("#,###.00");
            model.addRow(new Object[]{rs.getString(2), rs.getString(9),rs.getDate(14), rs.getDate(3), rs.getDate(4), rs.getString(5), df.format(rs.getDouble(12)), rs.getString(1), rs.getString(13)});
        }

    }

