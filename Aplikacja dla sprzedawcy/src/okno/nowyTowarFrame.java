    public void wczytajKategorie() throws SQLException {
        KategoriaDB kategoriaDB = new KategoriaDB(con);
        ArrayList<Kategoria> lista = kategoriaDB.selectKategoria();
        int liczba = lista.size();
        jComboBox2.removeAllItems();

        for (int i = 0; i < liczba; i++) {
            jComboBox2.addItem(lista.get(i));
        }
    }

