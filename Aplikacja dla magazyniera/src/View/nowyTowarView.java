    public Kategoria newKategoria() {

        JTextField jtfNazwa = new JTextField();
        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Nazwa"));
        panel.add(jtfNazwa);
        String strNazwa = "";

        int result = JOptionPane.showConfirmDialog(null, panel, "Nowa Kategoria",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (result == JOptionPane.OK_OPTION) {
            strNazwa = jtfNazwa.getText();
        }
        Kategoria kategoria = new Kategoria();
        kategoria.setNazwa(strNazwa);
        return kategoria;
    }

