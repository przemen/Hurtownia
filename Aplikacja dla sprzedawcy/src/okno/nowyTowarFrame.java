    private boolean validKod() throws SQLException {
        if (!txtKodTowaru.getText().equals("")) {
            if (new TowarDB(con).liczbaKod(txtKodTowaru.getText()) > 0) {
                JOptionPane.showMessageDialog(null, "Istnieje już towar o kodzie profuktu " + txtKodTowaru.getText(), "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            } else {
                return true;
            }
        } else {
            return false;
        }

    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Kategoria kat = (Kategoria) jComboBox2.getSelectedItem();
        String IdKategoria = kat.getId().toString();

        JednostkaMiary jednoMiary = (JednostkaMiary) jComboBox1.getSelectedItem();
        String IdJednostkaMiary = jednoMiary.getId().toString();

        String Nazwa = txtNazwa.getText();
        String Opis = teaOpis.getText();
        String kodTow = txtKodTowaru.getText();
        String kodKre = txtKodKreskowy.getText();
        String Pkwiu = txtPKWiU.getText();

        Object cmboitem = jComboBox3.getSelectedItem();

        String VAT = (String) cmboitem;
        try {
            if (validKod() && !Nazwa.equals("") && !VAT.equals("") && !IdJednostkaMiary.equals("")) {
                TowarDB towarDB = new TowarDB(con);
                towarDB.insertTowar(Nazwa, Opis, IdJednostkaMiary, kodTow, kodKre, Pkwiu, IdKategoria, VAT);

                dispose();
                try {
                    lt.wypiszzDB();
                } catch (SQLException ex) {
                    Logger.getLogger(nowyTowarFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{JOptionPane.showMessageDialog(null, "Proszę sprawdzić dane " + txtKodTowaru.getText(), "Error", JOptionPane.ERROR_MESSAGE);
}
        } catch (SQLException ex) {
            Logger.getLogger(nowyTowarFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

