    private void jXTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jXTable1MouseClicked
        // TODO add your handling code here:

       

        try { idFaktura = (String) jXTable1.getValueAt(jXTable1.getSelectedRow(), 7);

            editable = (String) jXTable1.getValueAt(jXTable1.getSelectedRow(), 8);

            if (editable.equals("null")) {
                editable = "1";
                jButton1.setEnabled(true);
            }  
            
            if(editable.equals("0")){
            jButton1.setEnabled(false);
            }
        } catch (NullPointerException e) {
            jButton1.setEnabled(true);
            editable = "1";
        }

        
        if (evt.getClickCount() == 2) {

            try {
                JFrame f = new JFrame();
                podgladFaktury lk = null;

                lk = new podgladFaktury(con, idFaktura, f);

                f.add(lk);
                f.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
                f.setSize(840, 480);
                f.setVisible(true);
                f.setTitle("Hurtownia - Podgląd Faktury");
                f.setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("icon.jpg")));
                f.setExtendedState(JFrame.MAXIMIZED_BOTH);
            } catch (SQLException ex) {
                Logger.getLogger(listaFaktur.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
    }//GEN-LAST:event_jXTable1MouseClicked

